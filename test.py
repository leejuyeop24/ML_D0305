## ----------------------------------------------------------------------------------
## 모듈로딩
## ----------------------------------------------------------------------------------
from Cat2 import *
from Dog2 import *

## ----------------------------------------------------------------------------------
## 객체 생성
## ----------------------------------------------------------------------------------
myDog = Dog2("똥개", "흰색", "갈색", 2.5, 1, "남자", "자루")
yourDog = Dog2("치와와", "검은색", "검은색", 1.6, 0.8, "여자", "깐순이")
myCat = Cat2("아바시니안", "갈색", "노랑색", 4.7, 4, "수컷", "아비")

## ----------------------------------------------------------------------------------
## 객체 속성 확인
## ----------------------------------------------------------------------------------
print(f"myDog 이름 : {myDog.name}")
print(f"myDog 나이 : {myDog.age}")

print(f"yourDog 이름 : {yourDog.name}")
print(f"yourDog 털색상 : {yourDog.coat_color}")

print(f"myCat 나이 : {myCat.age}")
print(f"myDog 성별 : {myCat.gender}")

## ----------------------------------------------------------------------------------
## 객체 속성 변경
## ----------------------------------------------------------------------------------
## 몸무게 변경
myDog.weight = 3.2

## 나이 변경
yourDog.age = 1

## ----------------------------------------------------------------------------------
## 객체 메서드 실행
## ----------------------------------------------------------------------------------
## 나의 강아지가 짖는다
myDog.bark()

## 나의 강아지가 꼬리친다.
yourDog.tailing()


## 나의 고양이가 꼬리친다.
myCat.tailing()
